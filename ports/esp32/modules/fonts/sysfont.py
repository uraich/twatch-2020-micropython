def get_ch(ch):
    oc = ord(ch)
    ioff = 2 * (oc + 1) if oc >= 0 and oc <= 254 else 0
    doff = ifb(_mvi[ioff : ])
    # print("ioff: %d, doff: %d"%(ioff,doff))
    width = ifb(_mvfont[doff : ])

    next_offs = doff + 2 + ((9 - 1)//8 + 1) * width
    # print("next_offs: ",next_offs)
    return _mvfont[doff + 2:next_offs], 8, width

